def op(x,y,z):

    if(z==1):
        return x+y
    elif(z==2):
        return x-y
    elif(z==3):
        return x*y
    elif(z==4):
        return x/y
    elif(z==5):
        return x%y
    elif(z==6):
        return x**y
    elif(z==7):
        return x//y
    
    else:
        print(" you have entered a wrong input")
        
    
